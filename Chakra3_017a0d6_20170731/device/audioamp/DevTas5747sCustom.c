BOOL _TAS5747s_RegInit(void)
{
    U8 pData[4] = {0,0,0,0};

    if(!DevTas5747sByteWrite(TAS5747s_BIQUAD_FILTER_CTRL_3, 0x00))    // 1 Byte I2C Êç? Ðã? - Î®??K ??
    {
        if(!DevTas5747sByteWrite(TAS5747s_BIQUAD_FILTER_CTRL_3, 0x00))
            return FALSE;
    }
    DevTas5747sByteWrite(TAS5747s_SOFT_RESET_CTRL, 0x01);     // soft reset, ack Error øË return ? Ú¶? Ï­?.

    msAPI_Timer_Delayms(30);

    DevTas5747sByteWrite(TAS5747s_BIQUAD_FILTER_CTRL_3, 0x00);    // 1 Byte I2C Êç? Ðã? - Î®??K ??
    DevTas5747sByteWrite(TAS5747s_MASTER_CLOCK_FREQUENCY_CTRL, 0x00);     // clock set
    DevTas5747sByteWrite(TAS5747s_AUTO_TRIM_SETTING, 0x03);      	// Trim setting (10kohm guarrantee for 16pin)
    DevTas5747sByteWrite(TAS5747s_MODULATION_LIMITE, 0x02);     	// MI = 98%
    DevTas5747sByteWrite(TAS5747s_LEGACY_SYSCTL2, 0x48);    	// #21 Pin : I2S Data out setting
    DevTas5747sByteWrite(TAS5747s_IC_DELAY, 0x80);    	// Inter channel Delay
    DevTas5747sByteWrite(TAS5747s_IC_DELAY2, 0xD4);     	//
    DevTas5747sByteWrite(TAS5747s_IC_DELAY3, 0x80);   	//
    DevTas5747sByteWrite(TAS5747s_IC_DELAY4, 0xD4);   	//
    DevTas5747sByteWrite(TAS5747s_PRESCALER_VALUE_CTRL, 0x80);  	// Amp Scale

 // Write DRC & PEQ setting
 #if ENABLE_PEQ
    _TAS5747s_WriteDrcTable();
     DevTas5747sByteWrite(TAS5747s_BIQUAD_FILTER_CTRL_3, 0x00);    // 1 Byte I2C Êç? Ðã? - Î®??K ??
    _TAS5747s_WritePeqTable();
 #endif
 //

    pData[1] = g_AmpPrescale;
    DevTas5747sByteWrite(TAS5747s_BIQUAD_FILTER_CTRL_3, 0x01);    // Enable Burst write

    DevTas5747sBurstWrite(TAS5747s_OUTPUT_POST_SCALE, pData);

    DevTas5747sByteWrite(TAS5747s_BIQUAD_FILTER_CTRL_3, 0x00);    // Enable Byte write

    DevTas5747sByteWrite(TAS5747s_CHANNEL1_VOLUME, g_AmpVolume); //Temp +6dB
    DevTas5747sByteWrite(TAS5747s_CHANNEL2_VOLUME, g_AmpVolume); //Temp +6dB

    DevTas5747sByteWrite(TAS5747s_HP_GAIN, 0x88); //HP gain (8dB)
    DevTas5747sByteWrite(TAS5747s_HP_ENABLE, 0x01); //HP enable

    DevTas5747sByteWrite(TAS5747s_MASTER_VOL_PWM_SWITCH, 0xFF);   // unmute

    return TRUE;
}

