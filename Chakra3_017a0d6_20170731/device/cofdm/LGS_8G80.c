LGS_RESULT LGS8G80_KModeScan(_LGS_DEVICEHANDLE_)
{
	UINT8 regAddr = 0,regData = 0,i; //datax;
	UINT8 waitms = 40,interval = 10;
	UINT32 afc_data;
	INT32 Check_BCH = 0;

	DemodParameters		para;
	LGS_RESULT err=LGS_NO_ERROR;

	LGSDbgPrint(( "\n K mode scan--------------------------" ));
	Check_BCH = Check_BCH;
	//LGS_Wait(100);
	LGS8G80_SoftReset();
	LGS_Wait(100);

	regAddr = 0x13;
	for (i=0; i<interval; i++)
	{
		LGS_Wait(waitms);
		if ((err = LGS_ReadRegister(LGS8G80SECADDR1, regAddr, &regData)))
		{
			LGSDbgPrint(( "\n Reg13 is error" ));
			goto failed;
		}
		{
			UINT8 Reg13 = (UINT8)regData & 0x06;
			if (Reg13 == 0x02)
			{
				LGSDbgPrint(("\n fail"));
				break;
			}
			else if (Reg13 == 0x04)
			{
				LGSDbgPrint(("\n done"));
				break;
			}
		}
	}

	if (i == interval)
	{
		LGSDbgPrint(("\n no GB signal"));
		goto failed;
	}

	if((err = LGS_ReadRegister( LGS8G80SECADDR1, 0x13, &pn_mode )))	goto failed;
	LGSDbgPrint(("\n Reg13 is %x",pn_mode));
	pn_mode &= 0x60;

	if ((err= LGS_ReadRegister(LGS8G80SECADDR1, 0xAF, &regData))) goto failed;

	pn_mode_freq = regData;
	LGSDbgPrint(("\n    ReadReg: Read 0xAF = %x",pn_mode_freq));

	if ((err = LGS_ReadRegister(LGS8G80SECADDR1, 0xAE, &regData))) goto failed;
	LGSDbgPrint(("\n    ReadReg: Read 0xAE = %x",regData));

	pn_mode_freq = (pn_mode_freq<<8)|regData;
	LGSDbgPrint(("\n    pn_mode_freq = (%2x)",pn_mode_freq));

	err = INTERNAL_LGS8G80_CheckBCH(LGS8G80_CHECKBCHWAIT,LGS8G80_CHECKBCHTIME_KMODE);
	if (LGS_NO_ERROR == err)
	{
		para.AFCPhase=0;
		LGS8G80_GetParameters(&para);
		afc_data = para.AFCPhase;

		if ((err = LGS_ReadRegister(LGS8G80SECADDR1, 0x13, &pn_mode))) goto failed;
		LGSDbgPrint(("\n Reg13 is %x",pn_mode));
		pn_mode &= 0x60;
		if (pn_mode == 0x00)
		{
			LGSDbgPrint(("\n K mode PN420"));
			if((err = LGS_RegisterBitClr( LGS8G80SECADDR1, 0x18, 0x01) ))	goto failed;//Constant PN Enable
			if((err = LGS_RegisterBitClr( LGS8G80SECADDR1, 0x19, 0x01 )))	goto failed;//Single Carrier Enable
		}
		else if (pn_mode == 0x20)
		{
			LGSDbgPrint(("\n K mode PN595"));

			if((err = LGS_RegisterBitSet( LGS8G80SECADDR1, 0x18, 0x01 )))	goto failed;//Constant PN Enable
			if((err = LGS_RegisterBitSet( LGS8G80SECADDR1, 0x19, 0x01 )))	goto failed;//Single Carrier Enable
		}
		else if (pn_mode == 0x40)
		{
			LGSDbgPrint(("\n K mode PN945"));

			if((err = LGS_RegisterBitClr( LGS8G80SECADDR1, 0x18, 0x01 )))	goto failed;//Constant PN Enable
			if((err = LGS_RegisterBitClr( LGS8G80SECADDR1, 0x19, 0x01 )))	goto failed;//Single Carrier Enable
		}


		if ((err = LGS_ReadRegister(LGS8G80SECADDR1, 0x18, &pn_phase))) goto failed;
		LGSDbgPrint(("\n Reg18 is %x",pn_phase));
		pn_phase &= 0x01;
		if (pn_phase == 0)
		{
			LGSDbgPrint(("\n K mode variable"));
		}
		else if (pn_phase == 1)
		{
			LGSDbgPrint(("\n K mode constant"));
		}

		if ((err = LGS_ReadRegister(LGS8G80SECADDR1, 0x19, &carrier_mode))) goto failed;
		LGSDbgPrint(("\n Reg19 is %x",carrier_mode));
		carrier_mode &= 0x01;
		if (carrier_mode == 0)
		{
			LGSDbgPrint(("\n K mode MC"));
		}
		else if (carrier_mode == 1)
		{
			LGSDbgPrint(("\n K mode SC"));
		}

		if ((err = LGS_ReadRegister(LGS8G80SECADDR1, 0x1F, &tps_mode))) goto failed;
		LGSDbgPrint(("\n K mode tps_mode = (%02x)",tps_mode));
		modulation_mode = tps_mode & 0x1C;
		if (modulation_mode == 0x00)
		{
			LGSDbgPrint(("\n K mode modulation_mode 4QAM"));
		}
		else if (modulation_mode == 0x04)
		{
			LGSDbgPrint(("\n K mode modulation_mode 4QAM-NR"));
		}
		else if (modulation_mode == 0x08)
		{
			LGSDbgPrint(("\n K mode modulation_mode 16QAM"));
		}
		else if (modulation_mode == 0x0C)
		{
			LGSDbgPrint(("\n K mode modulation_mode 32QAM"));
		}
		else if (modulation_mode == 0x10)
		{
			LGSDbgPrint(("\n K mode modulation_mode 64QAM"));
		}

		FEC_rate = tps_mode & 0x03;
		if (FEC_rate == 0)
		{
			LGSDbgPrint(("\n K mode FEC_rate 0.4"));
		}
		else if (FEC_rate == 1)
		{
			LGSDbgPrint(("\n K mode FEC_rate 0.6"));
		}
		else if (FEC_rate == 2)
		{
			LGSDbgPrint(("\n K mode FEC_rate 0.8"));
		}

		time_interleaver = tps_mode & 0x20;
		if (time_interleaver == 0)
		{
			LGSDbgPrint(("\n K mode time_interleaver 240"));
		}
		else
		{
			LGSDbgPrint(("\n K mode time_interleaver 720"));
		}
	}

failed:
	return err;
}


