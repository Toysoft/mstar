	if( (err = LGS_WriteRegister( LGS8G80SECADDR2, 0x44, 0xe0 )))	goto failed;
	if( (err = LGS_WriteRegister( LGS8G80SECADDR2, 0x4e, 0xe0 )))	goto failed;
