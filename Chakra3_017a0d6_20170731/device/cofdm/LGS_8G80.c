	if (LGS_WriteRegister(LGS8G80SECADDR1, 0x09, tmp[1]))goto failed;
	if (LGS_WriteRegister(LGS8G80SECADDR1, 0x0A, tmp[2]))goto failed;
	if (LGS_WriteRegister(LGS8G80SECADDR1, 0x0B, tmp[3]))goto failed;
