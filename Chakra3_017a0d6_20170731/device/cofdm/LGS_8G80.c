		if( (err = LGS_WriteRegister( LGS8G80SECADDR1, 0x17, g_Reg_3A_17 )))	goto failed;
		if( (err = LGS_WriteRegister( LGS8G80SECADDR1, 0x4b, g_Reg_3A_4B )))	goto failed;
		if( (err = LGS_WriteRegister( LGS8G80SECADDR1, 0x4c, g_Reg_3A_4C )))	goto failed;
