	if(( err = LGS_WriteRegister( LGS8G80SECADDR2, 0xb9, 0x00 )))	goto failed;
	if( (err = LGS_WriteRegister( LGS8G80SECADDR2, 0xb8, 0x0a )))	goto failed;
	if( (err = LGS_WriteRegister( LGS8G80SECADDR2, 0xbb, 0x03 )))	goto failed;
	if(( err = LGS_WriteRegister( LGS8G80SECADDR2, 0xba, 0x03) ))	goto failed;

	if( (err = LGS_WriteRegister( LGS8G80SECADDR2, 0xff, 0x0c )))	goto failed;
	if(( err = LGS_WriteRegister( LGS8G80SECADDR2, 0x22, 0x38 )))	goto failed;
	if( (err = LGS_WriteRegister( LGS8G80SECADDR2, 0x42, 0xe0 )))	goto failed;
	if( (err = LGS_WriteRegister( LGS8G80SECADDR2, 0x4c, 0xe0 )))	goto failed;
