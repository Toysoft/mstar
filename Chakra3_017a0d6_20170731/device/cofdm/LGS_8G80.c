	if( (err = LGS_WriteRegister( LGS8G80SECADDR2, 0x46, 0xf0 )))	goto failed;
	if( (err = LGS_WriteRegister( LGS8G80SECADDR2, 0x50, 0xf0 )))	goto failed;
