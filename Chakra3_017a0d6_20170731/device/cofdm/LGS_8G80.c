	if( (err = LGS_WriteRegister( LGS8G80SECADDR2, 0x20, 0x38) ))	goto failed;
	if( (err = LGS_WriteRegister( LGS8G80SECADDR2, 0x2a, 0x0a )))	goto failed;
	if(( err = LGS_WriteRegister( LGS8G80SECADDR2, 0x2c, 0x0a )))	goto failed;
	if( (err = LGS_WriteRegister( LGS8G80SECADDR2, 0x2e, 0x0a )))	goto failed;
