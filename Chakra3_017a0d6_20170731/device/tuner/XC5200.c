unsigned int xc_read_reg(unsigned int regAddr)//, U16 *i2cData)
{
    unsigned char buf[2];
    unsigned char u8Data[2];


    buf[0] = (regAddr >> 8) & 0xFF;
    buf[1] = regAddr & 0xFF;

    if(MDrv_IIC_ReadBytes(0x02C2/*XC5000_I2C_ADDR*/, 2, buf, 2, u8Data)==FALSE)
        printf("\t --->>>xc5200 iic read failed\n");

    return ( (((unsigned int)u8Data[0])<<8)|u8Data[1] );
}

unsigned int xc_write_reg(unsigned int regAddr, unsigned int i2cData)
{
  unsigned char buf[4];
  BOOLEAN XC_WRITE_OK = TRUE;

  buf[0] = (regAddr >> 8) & 0xFF;
  buf[1] = regAddr & 0xFF;
  buf[2] = (i2cData >> 8) & 0xFF;
  buf[3] = i2cData & 0xFF;

  if(MDrv_IIC_WriteBytes(0x02C2/*XC5000_I2C_ADDR*/, 0, NULL, 4, buf)==FALSE)
  {
    printf("\t ---->>xc5200 iic failed\n");
    XC_WRITE_OK = FALSE;
  }

  return XC_WRITE_OK;
}

