void btnTunerInitClick(void)
{
    unsigned int result;

	msAPI_Timer_Delayms(1);

    msAPI_Timer_Delayms(1);

    // Download tuner FW
    if(TRUE != xc_load_i2c_sequence(XC5000_firmware_SEQUENCE))
    {
        printf("Download tuner FW FAIL\r\n");
        msAPI_Timer_Delayms(150);
        if(TRUE != xc_load_i2c_sequence(XC5000_firmware_SEQUENCE))
        {
            printf("Download tuner FW FAIL again\r\n");
        }
    }
    else
    {
        printf("Download tuner FW OK\r\n");
    }
    msAPI_Timer_Delayms(150);
    // Check busy flag
    result = xc_Check_Busy();
    if(result == 0)
    {
		printf("Tuner not busy\r\n");
    }
    else
    {
		printf("Tuner busy AAA\r\n");
    }


    // Initialize
    if(XC_RESULT_SUCCESS != xc_initialize())
    {
		printf("XC5000 Initialize Error\r\n");
    }
    else
    {
        printf("XC5000 Initialize OK\r\n");
    }

    msAPI_Timer_Delayms(100);
    // Check busy flag
    result = xc_Check_Busy();
    if(result == 0)
    {
		printf("Tuner not busy\r\n");
    }
    else
    {
        //buf += ", Tuner busy\n";
		printf("Tuner busy BBB\r\n");
    }

    msAPI_Timer_Delayms(1);

    printf(" version number----  0x%x\n", xc_get_version());
    printf(" product id----  0x%x\n", xc_get_product_id());
    if(xc_get_checksum_firmware() == 0x0001)
        printf(" good firmware loaded! \n");
    else
        printf(" firmware loaded checksum error! \n");

}


