void devDigitalTuner_SetFreq ( double Freq,  RF_CHANNEL_BANDWIDTH eBandWidth)
{
    double FineFreq;
    U8 u8StandardValue;

    //in dtv mode , the RF_FREQ = (fc + 1.25MHz - BW/2)/15625      BW:eBandwidth
    switch(eBandWidth)
    {
        case E_RF_CH_BAND_6MHz:
            FineFreq = 1.75;
            u8StandardValue = 0x11;
            break;
        case E_RF_CH_BAND_7MHz:
            FineFreq = 2.25;
            u8StandardValue = 0x14;
            break;
        case E_RF_CH_BAND_8MHz:
            FineFreq = 2.75;
            u8StandardValue = 0x12;
            break;
        default:
            FineFreq = 2.25;
            u8StandardValue = 0x13;
            break;
    }

    printf("set dtv freq=%f finefreq=%f\n", Freq, FineFreq);//morgan.smc test

    xc_set_RF_frequency(Freq-FineFreq);
}

