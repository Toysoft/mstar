/** ===========================================================================
 *
 * Name         : DecodeStr
 *
 * description    : Decode compressed string by huffman-based Text compression
 *                    (ATSC A/65B. Annex C,F)
 *
 * @param in    : address pointer of the compressed string buffer
 * @param in    : compressed string length
 * @param in    : compression type
 * @param in    : address pointer of the decompressed string buffer
 *
 * @param out    : decompressed string length
 *  ============================================================================
 */
SINT32 DecodeStr(    const UINT08    *pbufEncStr,    /* compressed string */
                    SINT32        nLenStr,        /* the number of bytes in compressed string    */
                    UINT08        nEncodeType,    /* compression type */
                    UINT08        *pbufDecStr,    /* decompressed string */
                    SINT32        nMaxBufDecStr)    /* decompressed string length */
{
    SINT32    nLenDecStr= 0;    /* the length of the decompressed string */

    /* Check compression type */
    if ( nEncodeType == NO_COMPRESSION )
    {
        memcpy( (UINT08 *)pbufDecStr, (UINT08 *)pbufEncStr, nLenStr );
        nLenDecStr = nLenStr;
    }
    /* decompression using type1 decode table */
    else if ( nEncodeType == HUFFMAN_TITLE )
    {
        nLenDecStr = DecodeStrByTable( pbufEncStr, nLenStr, pbufDecStr, nMaxBufDecStr,
                            gDecodeTblType1);
    }
    /* decompression using type2 decode table */
    else if ( nEncodeType == HUFFMAN_DESCRIPTION )
    {
        nLenDecStr = DecodeStrByTable( pbufEncStr, nLenStr, pbufDecStr, nMaxBufDecStr,
                            gDecodeTblType2);
    }
    return nLenDecStr;
}

/** ===========================================================================
 *
 * Name         : DecodeStrByTable
 *
 * description    : Decode compressed string using decode table
 *
 * @param in    : address pointer of the compressed string buffer
 * @param in    : compressed string length
 * @param in    : address pointer of the decompressed string buffer
 * @param in    : decode table
 * @param in    : the size of decode table
 *
 * @param out    : decompressed string length
 *  ============================================================================
 */
SINT32 DecodeStrByTable(const UINT08    *pbufEncStr, SINT32        nLenStr,
                        UINT08        *pbufDecStr, SINT32        nMaxBufDecStr,
                        const UINT08    *tblDecodeTree)
{
    SINT32    i, j, nIdxBase, nOffset, nMaxBits;
    SINT32  nLenDecStr;    /* the number of bytes in decompressed string */
    UINT08    cDecData, nEncBit, nChild;

    /* initialize output buffer and length */
    *pbufDecStr = 0;
    nLenDecStr     = 0;

    /* Get tree root using prior cDecData */
    InitializeTreeRoot( TERM, tblDecodeTree, &nIdxBase, &nOffset );

    /* for each encoded character */
    nMaxBits = nLenStr * 8;
    for ( i = 0; i < nMaxBits; i++ )
    {
        /* get value of current bit */
        nEncBit = GetNthBit( pbufEncStr, i );
        /* get child word offset or leaf */
        nChild = tblDecodeTree[ nIdxBase + nOffset + nEncBit ];

        /* find leaf */
        if ( nChild & 0x80 )
        {
            /* decode a character */
            cDecData = nChild - 128;
            /* decoded data is escape character */
            if ( cDecData == ESC )
            {
                /* following 8 bits are uncompressed */
                /* if ( i + 8 < nMaxBits ) */
                /* Characters(128 ~ 255) cannot be compressed */
                while( ( cDecData >= 0x80 ) || ( cDecData == ESC ) )
                {
                    if( (i + 8) >= nMaxBits )
                    {
                        /* if TERM not exist, add TERM code and return length ( by hrlee 99.10.1 ) */
                        *pbufDecStr = TERM;
                        pbufDecStr++; nLenDecStr++;
                        ERR_Msg("ERROR : Out of range in GetNthBit!!\r\n");
                        return nLenDecStr;
                    }
                    cDecData = 0;
                    for ( j = 1; j <= 8; j++ )
                    {
                        /* get value of current bit */
                        nEncBit = GetNthBit( pbufEncStr, i + j );
                        /* make ASCII code */
                        if ( nEncBit )
                            cDecData += nEncBit << ( 8 - j );
                    }
                    /* append decoded character */
                    *pbufDecStr    = cDecData;
                    pbufDecStr++;
                    nLenDecStr++;
                    if(nLenDecStr==nMaxBufDecStr-1)
                    {
                        *pbufDecStr = TERM;
                        nLenDecStr++;
                        return nLenDecStr;
                    }

                    /* if decoded data is a terminate character, decoding is finished */
                    if ( cDecData == TERM )
                    {
                        return nLenDecStr;
                    }
                    /* adjust index */
                    i += 8;
                } /* end of while */
            } /* end of if */
            else
            {    /* append decoded character */
                *pbufDecStr = cDecData;
                pbufDecStr++;
                nLenDecStr++;
                if(nLenDecStr==nMaxBufDecStr-1)
                {
                    *pbufDecStr = TERM;
                    nLenDecStr++;
                    return nLenDecStr;
                }
                /* if decoded data is a terminate character, decoding is finished */
                if ( cDecData == TERM )
                {
                    return nLenDecStr;
                }
            }
            /* Get tree root using prior cDecData */
            InitializeTreeRoot( cDecData, tblDecodeTree, &nIdxBase, &nOffset );
        }
        else  /* find offset to traverse */
        {
            nOffset = nChild * 2;
        }
    }  /* end of for loop */

    /* if TERM not exist, add TERM code and return length */
    *pbufDecStr = TERM;
    pbufDecStr++;
    nLenDecStr++;
    ERR_Msg("ERROR : TERM Code not exist!!\r\n");
    return nLenDecStr;
}

/** ===========================================================================
 *
 * Name         : InitializeTreeRoot
 *
 * description    : Get tree root using prior character
 *
 * @param in    : default prior character
 * @param in    : decode table
 * @param in    : offset of tree root
 * @param in    : initialize offset for traverse
 *
 * @param out    : none
 *  ============================================================================
 */
void InitializeTreeRoot(    UINT08 cPrevData,    const UINT08    *tblDecodeTree,
                            SINT32 *pnIdxBase,    SINT32 *pnOffset )
{
    /* get offset of tree root for prior character */
    *pnIdxBase = ( tblDecodeTree[ cPrevData * 2 ] << 8 )
                    + tblDecodeTree[ cPrevData * 2 + 1 ];

    /* get offset for traverse */
    *pnOffset = 0;
}

/** ===========================================================================
 *
 * Name         : GetNthBit
 *
 * description    : Get N-th bit in given byte stream
 *                    The index is counted by left to right
 *
 * @param in    : address pointer of encode string buffer
 * @param in    : index for bit position
 *
 * @param out    : the value of the N-th bit
 *  ============================================================================
 */
UINT08 GetNthBit( const UINT08 *pbufEncStr, SINT32 nIdx )
{
    return ( pbufEncStr[ nIdx / 8 ] >> ( 7 - nIdx % 8 ) ) & 0x01;
}

