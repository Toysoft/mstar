/* Static functions */
static U16 _msAPI_MLE_FontLatineComposedChar(U16 nCode1, U16 nCode2)
{
    U16 nChar = 0x0000;
    U8 nRow1, nRow2, nCol1, nCol2;
    U8 nIndex1, nIndex2;

    nRow1 = (U8)(nCode1>>8);
    nRow2 = (U8)(nCode2>>8);
    nCol1 = (U8)(nCode1&0x00FF);
    nCol2 = (U8)(nCode2&0x00FF);

    if ( /*(nRow1 >= 0x00) && */(nRow1 <= 0x30)
        && (nRow2 == 0x03) && /*(nCol2 >= 0x00) && */(nCol2 <= 0x31) )
    {
        switch ( nRow1 )
        {
        case 0x00:
            if (nCol1>=0x40)
                nIndex1 = UNICODE_COMBINE_FIRST_UCS2_00[nCol1-0x40];
            else
                nIndex1 = 0xff;
            break;

        case 0x01:
            nIndex1 = UNICODE_COMBINE_FIRST_UCS2_01[nCol1];
            break;

        case 0x02:
            if (nCol1>=0x20)
                nIndex1 = UNICODE_COMBINE_FIRST_UCS2_02[nCol1-0x20];
            else
                nIndex1 = 0xff;
            break;

        case 0x03:
            nIndex1 = UNICODE_COMBINE_FIRST_UCS2_03[nCol1];
            break;

        default:
            nIndex1 = 0xff;
            break;
        }

        if ( nIndex1 != 0xff )
        {
            nIndex2 = UNICODE_COMBINE_SECOND_UCS2[nCol2];
            if ( nIndex2 != 0xff )
            {
                  if (nIndex2 < UNICODE_COMBINE_UCS2_COUNT[nIndex1])
                    nChar = UNICODE_COMBINE_UCS2[nIndex1][nIndex2];
            }
        }
    }

    return nChar;
}
