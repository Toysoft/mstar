// Assume all parameters are valid
U16 _msAPI_MLE_FontLatineGetNextCodes(const U16 *pInTxt, U32 InLen, U16 *pOutCode, U32 OutLen, U32 *paConsumed)
{
    U16 Len;

    if(InLen <= 1)
        return 0;

    if(!IS_COMBINE_MARK(pInTxt[1]))
        return 0;

    OutLen = OutLen;
    
    pOutCode[0] = _msAPI_MLE_FontLatineComposedChar(pInTxt[0], pInTxt[1]);

    if (pOutCode[0] == 0x0000)
    {
        pOutCode[0] = pInTxt[0];
        
        Len = 1;
        paConsumed[0] = 1;
        paConsumed[1] = 1;
    }
    else
    {
        Len = 1;
        paConsumed[0] = 2;
        paConsumed[1] = 2;
    }
            
    return Len;
}

