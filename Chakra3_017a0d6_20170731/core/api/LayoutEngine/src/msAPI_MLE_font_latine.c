U16 msAPI_MLE_FontLatineVectorGetNextCodes(const U16 *pInTxt, U32 InLen, U16 *pOutCode, U32 OutLen, U32 *paConsumed)
{
    U16 U16ReturnLen = 0;

    U16ReturnLen = _msAPI_MLE_FontLatineGetNextCodes(pInTxt, InLen, pOutCode, OutLen, paConsumed);

    if ( msAPI_MLE_FONTBASE_IsVectorFontExist() )
    {
        U16 i;
        for ( i = 0; i < U16ReturnLen; i++ )
        {
            if( msAPI_MLE_FontBaseIsControlChar(pOutCode[i]) )
            {
                pOutCode[i] = (U16)(-2); //Control code
            }
            else
            {
                pOutCode[i] = msAPI_MLE_FONTBASE_VectorFontGetGlyphId(pOutCode[i]);
            }
        }
    }
    
    return U16ReturnLen;
}
