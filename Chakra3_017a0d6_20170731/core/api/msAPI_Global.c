typedef U32 TypCRC;

#define WIDTH       (8 * sizeof(TypCRC))
#define TOPBIT      (1 << (WIDTH - 1))
#define POLYNOMIAL  0x4C11DB7

void crcInit(void)
{
    TypCRC remainder;
    int dividend;
    U8 u8Bit;
    TypCRC crcTable[256];


    /*
     * Compute the remainder of each possible dividend.
     */
    for ( dividend = 0; dividend < 256; ++dividend )
    {
        /*
         * Start with the dividend followed by zeros.
         */
        remainder = dividend << (WIDTH - 8);

        /*
         * Perform modulo-2 division, a bit at a time.
         */
        for ( u8Bit = 8; u8Bit > 0; --u8Bit)
        {
            /*
             * Try to divide the current data bit.
             */
            if (remainder & TOPBIT)
            {
                remainder = (remainder << 1) ^ POLYNOMIAL;
            }
            else
            {
                remainder = (remainder << 1);
            }
        }

        /*
         * Store the result into the table.
         */
        crcTable[dividend] = remainder;
    }

/*
    printf("U32 crc32_table[256]=\n{\n");
    for ( dividend = 0; dividend < 256; dividend += 4 )
    {
        printf("    0x%08X, 0x%08X, 0x%08X, 0x%08X,\n",
            crcTable[dividend],
            crcTable[dividend+1],
            crcTable[dividend+2],
            crcTable[dividend+3] );
    }
    printf("};\n");
    */

} /* crcInit() */

//TypCRC crcFast(U8 const message[], U32 nBytes)
U32 msAPI_CRC_Cal(const U8* pu8DataBuf, U32 u32DataSize)
{
    U32 u32DataIdx;
    U8 data;
    //U32 remainder = 0;
    U32 remainder = 0xFFFFFFFF;


    /*
     * Divide the message by the polynomial, a byte at a time.
     */
    for ( u32DataIdx = 0; u32DataIdx < u32DataSize; ++u32DataIdx)
    {
        data = pu8DataBuf[u32DataIdx] ^ (remainder >> (WIDTH - 8));
        remainder = crc32_table[data] ^ (remainder << 8);
    }

    /*
     * The final remainder is the CRC.
     */
    return (remainder);

}   /* crcFast() */
